"""Tests for workflow-to-skill auto-generation (synapse/workflow_skill_sync.py)."""

from __future__ import annotations

from pathlib import Path

import pytest


@pytest.fixture()
def project_dir(tmp_path: Path) -> Path:
    """Return a temporary project directory."""
    return tmp_path / "project"


def _make_workflow(
    name: str = "post-impl",
    description: str = "Post-implementation workflow",
    trigger: str = "",
    auto_spawn: bool = False,
):
    from synapse.workflow import Workflow, WorkflowStep

    return Workflow(
        name=name,
        steps=[WorkflowStep(target="claude", message="Review code")],
        description=description,
        trigger=trigger,
        auto_spawn=auto_spawn,
        scope="project",
    )


# ── generate_skill_md ────────────────────────────────────────


class TestGenerateSkillMd:
    def test_basic_frontmatter(self) -> None:
        """Should contain name and description in frontmatter."""
        from synapse.workflow_skill_sync import generate_skill_md

        wf = _make_workflow()
        md = generate_skill_md(wf)
        assert "name: post-impl" in md
        assert "Workflow: Post-implementation workflow." in md
        assert "Triggered by /post-impl command." in md

    def test_contains_autogen_marker(self) -> None:
        """Should contain the autogen marker."""
        from synapse.workflow_skill_sync import _AUTOGEN_MARKER, generate_skill_md

        wf = _make_workflow()
        md = generate_skill_md(wf)
        assert _AUTOGEN_MARKER in md

    def test_trigger_keywords_in_description(self) -> None:
        """Trigger keywords should appear in description."""
        from synapse.workflow_skill_sync import generate_skill_md

        wf = _make_workflow(trigger="実装が終わった|post-impl")
        md = generate_skill_md(wf)
        assert "Use this skill when: 実装が終わった|post-impl." in md

    def test_no_trigger_keywords(self) -> None:
        """Without trigger, no 'Use this skill when' text."""
        from synapse.workflow_skill_sync import generate_skill_md

        wf = _make_workflow(trigger="")
        md = generate_skill_md(wf)
        assert "Use this skill when:" not in md

    def test_auto_spawn_flag_in_instructions(self) -> None:
        """auto_spawn=True should add --auto-spawn to run command."""
        from synapse.workflow_skill_sync import generate_skill_md

        wf = _make_workflow(auto_spawn=True)
        md = generate_skill_md(wf)
        assert "--auto-spawn" in md

    def test_no_auto_spawn_flag(self) -> None:
        """auto_spawn=False should not add --auto-spawn."""
        from synapse.workflow_skill_sync import generate_skill_md

        wf = _make_workflow(auto_spawn=False)
        md = generate_skill_md(wf)
        assert "--auto-spawn" not in md

    def test_fallback_description(self) -> None:
        """When description is empty, use workflow name."""
        from synapse.workflow_skill_sync import generate_skill_md

        wf = _make_workflow(name="my-wf", description="")
        md = generate_skill_md(wf)
        assert "Workflow: my-wf." in md


