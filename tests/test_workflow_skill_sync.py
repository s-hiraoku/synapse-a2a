class TestIsAutogeneratedSkill:
    def test_autogenerated(self, project_dir: Path) -> None:
        """Directory with autogen marker should return True."""
        from synapse.workflow_skill_sync import _AUTOGEN_MARKER, is_autogenerated_skill

        skill_dir = project_dir / ".claude" / "skills" / "test"
        skill_dir.mkdir(parents=True)
        (skill_dir / "SKILL.md").write_text(
            f"---\nname: test\n---\n{_AUTOGEN_MARKER}\n# test\n"
        )
        assert is_autogenerated_skill(skill_dir) is True

    def test_hand_written(self, project_dir: Path) -> None:
        """Directory without autogen marker should return False."""
        from synapse.workflow_skill_sync import is_autogenerated_skill

        skill_dir = project_dir / ".claude" / "skills" / "test"
        skill_dir.mkdir(parents=True)
        (skill_dir / "SKILL.md").write_text("---\nname: test\n---\n# test\n")
        assert is_autogenerated_skill(skill_dir) is False

    def test_missing_directory(self, project_dir: Path) -> None:
        """Non-existent directory should return False."""
        from synapse.workflow_skill_sync import is_autogenerated_skill

        assert is_autogenerated_skill(project_dir / "nonexistent") is False

    def test_missing_skill_md(self, project_dir: Path) -> None:
        """Directory without SKILL.md should return False."""
        from synapse.workflow_skill_sync import is_autogenerated_skill

        skill_dir = project_dir / ".claude" / "skills" / "test"
        skill_dir.mkdir(parents=True)
        assert is_autogenerated_skill(skill_dir) is False


# ── sync_workflow_skill ──────────────────────────────────────


class TestSyncWorkflowSkill:
    def test_creates_skill_in_both_dirs(self, project_dir: Path) -> None:
        """Should create SKILL.md in both .claude/skills/ and .agents/skills/."""
        from synapse.workflow_skill_sync import sync_workflow_skill

        wf = _make_workflow(name="my-wf")
        written = sync_workflow_skill(wf, project_dir)

        assert len(written) == 2
        assert (project_dir / ".claude" / "skills" / "my-wf" / "SKILL.md").is_file()
        assert (project_dir / ".agents" / "skills" / "my-wf" / "SKILL.md").is_file()

    def test_updates_existing_autogen(self, project_dir: Path) -> None:
        """Should overwrite existing autogenerated skill."""
        from synapse.workflow_skill_sync import (
            _AUTOGEN_MARKER,
            sync_workflow_skill,
        )

        skill_dir = project_dir / ".claude" / "skills" / "my-wf"
        skill_dir.mkdir(parents=True)
        (skill_dir / "SKILL.md").write_text(f"old\n{_AUTOGEN_MARKER}\n")

        wf = _make_workflow(name="my-wf", description="Updated")
        sync_workflow_skill(wf, project_dir)

        content = (skill_dir / "SKILL.md").read_text()
        assert "Updated" in content

    def test_protects_hand_written_skill(self, project_dir: Path) -> None:
        """Should NOT overwrite hand-written skill (no marker)."""
        from synapse.workflow_skill_sync import sync_workflow_skill

        skill_dir = project_dir / ".claude" / "skills" / "my-wf"
        skill_dir.mkdir(parents=True)
        original = "---\nname: my-wf\n---\n# Custom skill\n"
        (skill_dir / "SKILL.md").write_text(original)

        wf = _make_workflow(name="my-wf")
        written = sync_workflow_skill(wf, project_dir)

        # .claude/skills should be skipped, .agents/skills should be created
        claude_paths = [p for p in written if ".claude" in str(p)]
        assert len(claude_paths) == 0
        assert (skill_dir / "SKILL.md").read_text() == original


# ── remove_workflow_skill ────────────────────────────────────


class TestRemoveWorkflowSkill:
    def test_removes_autogenerated(self, project_dir: Path) -> None:
        """Should remove autogenerated skill directories."""
        from synapse.workflow_skill_sync import (
            _AUTOGEN_MARKER,
            remove_workflow_skill,
        )

        for base in (".claude/skills", ".agents/skills"):
            skill_dir = project_dir / base / "my-wf"
            skill_dir.mkdir(parents=True)
            (skill_dir / "SKILL.md").write_text(f"x\n{_AUTOGEN_MARKER}\n")

        removed = remove_workflow_skill("my-wf", project_dir)
        assert len(removed) == 2
        assert not (project_dir / ".claude" / "skills" / "my-wf").exists()
        assert not (project_dir / ".agents" / "skills" / "my-wf").exists()

    def test_protects_hand_written(self, project_dir: Path) -> None:
        """Should NOT remove hand-written skill."""
        from synapse.workflow_skill_sync import remove_workflow_skill

        skill_dir = project_dir / ".claude" / "skills" / "my-wf"
        skill_dir.mkdir(parents=True)
        (skill_dir / "SKILL.md").write_text("---\nname: my-wf\n---\n# Custom\n")

        removed = remove_workflow_skill("my-wf", project_dir)
        assert len(removed) == 0
        assert skill_dir.exists()

    def test_nonexistent_is_noop(self, project_dir: Path) -> None:
        """Removing non-existent skill should be a no-op."""
        from synapse.workflow_skill_sync import remove_workflow_skill

        removed = remove_workflow_skill("ghost", project_dir)
        assert removed == []


# ── sync_all_workflows ───────────────────────────────────────


class TestSyncAllWorkflows:
    def test_syncs_all_and_removes_orphans(self, tmp_path: Path) -> None:
        """Should sync all workflows and remove orphans."""
        from synapse.workflow import Workflow, WorkflowStep, WorkflowStore
        from synapse.workflow_skill_sync import (
            _AUTOGEN_MARKER,
            sync_all_workflows,
        )

        project_dir = tmp_path / "project"
        wf_dir = project_dir / ".synapse" / "workflows"
        wf_dir.mkdir(parents=True)

        store = WorkflowStore(
            project_dir=wf_dir,
            user_dir=tmp_path / "user" / ".synapse" / "workflows",
        )

        # Save two workflows
        store.save(
            Workflow(
                name="wf-a",
                steps=[WorkflowStep(target="claude", message="a")],
                scope="project",
            )
        )
        store.save(
            Workflow(
                name="wf-b",
                steps=[WorkflowStep(target="gemini", message="b")],
                scope="project",
            )
        )

        # Create an orphaned autogenerated skill
        orphan_dir = project_dir / ".claude" / "skills" / "old-wf"
        orphan_dir.mkdir(parents=True)
        (orphan_dir / "SKILL.md").write_text(f"old\n{_AUTOGEN_MARKER}\n")

        written, removed = sync_all_workflows(
            project_dir,
            user_dir=tmp_path / "user" / ".synapse" / "workflows",
        )

        # Each workflow creates 2 files (.claude + .agents)
        assert len(written) == 4
        assert len(removed) == 1
        assert orphan_dir in removed
        assert not orphan_dir.exists()

    def test_preserves_hand_written_orphan(self, tmp_path: Path) -> None:
        """Should NOT remove hand-written skills even if no matching workflow."""
        from synapse.workflow_skill_sync import sync_all_workflows

        project_dir = tmp_path / "project"
        wf_dir = project_dir / ".synapse" / "workflows"
        wf_dir.mkdir(parents=True)

        # Create a hand-written skill (no matching workflow)
        skill_dir = project_dir / ".claude" / "skills" / "custom"
        skill_dir.mkdir(parents=True)
        (skill_dir / "SKILL.md").write_text("---\nname: custom\n---\n# Custom\n")

        _written, removed = sync_all_workflows(
            project_dir,
            user_dir=tmp_path / "user" / ".synapse" / "workflows",
        )

        assert removed == []
        assert skill_dir.exists()


