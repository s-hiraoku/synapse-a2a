class TestCLIIntegration:
    def test_create_generates_skill(self, tmp_path: Path) -> None:
        """cmd_workflow_create should auto-generate skill."""
        import argparse
        from unittest.mock import patch

        from synapse.commands.workflow import cmd_workflow_create
        from synapse.workflow import WorkflowStore
        from synapse.workflow_skill_sync import is_autogenerated_skill

        project_dir = tmp_path / "project"
        wf_dir = project_dir / ".synapse" / "workflows"
        store = WorkflowStore(
            project_dir=wf_dir,
            user_dir=tmp_path / "user" / ".synapse" / "workflows",
        )

        args = argparse.Namespace(
            workflow_name="test-wf",
            user=False,
            project=False,
            force=False,
        )

        with (
            patch("synapse.commands.workflow._get_workflow_store", return_value=store),
            patch("synapse.workflow_skill_sync.Path.cwd", return_value=project_dir),
            patch("synapse.commands.workflow.Path.cwd", return_value=project_dir),
        ):
            cmd_workflow_create(args)

        skill_dir = project_dir / ".claude" / "skills" / "test-wf"
        assert skill_dir.exists()
        assert is_autogenerated_skill(skill_dir)

    def test_delete_removes_skill(self, tmp_path: Path) -> None:
        """cmd_workflow_delete should remove auto-generated skill."""
        import argparse
        from unittest.mock import patch

        from synapse.commands.workflow import cmd_workflow_delete
        from synapse.workflow import Workflow, WorkflowStep, WorkflowStore
        from synapse.workflow_skill_sync import _AUTOGEN_MARKER

        project_dir = tmp_path / "project"
        wf_dir = project_dir / ".synapse" / "workflows"
        store = WorkflowStore(
            project_dir=wf_dir,
            user_dir=tmp_path / "user" / ".synapse" / "workflows",
        )

        # Create workflow + skill
        store.save(
            Workflow(
                name="victim",
                steps=[WorkflowStep(target="claude", message="x")],
                scope="project",
            )
        )
        skill_dir = project_dir / ".claude" / "skills" / "victim"
        skill_dir.mkdir(parents=True)
        (skill_dir / "SKILL.md").write_text(f"x\n{_AUTOGEN_MARKER}\n")

        args = argparse.Namespace(
            workflow_name="victim",
            user=False,
            project=False,
            force=True,
        )

        with (
            patch("synapse.commands.workflow._get_workflow_store", return_value=store),
            patch("synapse.commands.workflow.Path.cwd", return_value=project_dir),
        ):
            cmd_workflow_delete(args)

        assert not skill_dir.exists()

    def test_sync_command(self, tmp_path: Path, capsys: pytest.CaptureFixture) -> None:
        """cmd_workflow_sync should sync all workflows."""
        import argparse
        from unittest.mock import patch

        from synapse.commands.workflow import cmd_workflow_sync
        from synapse.workflow import Workflow, WorkflowStep, WorkflowStore

        project_dir = tmp_path / "project"
        wf_dir = project_dir / ".synapse" / "workflows"
        store = WorkflowStore(
            project_dir=wf_dir,
            user_dir=tmp_path / "user" / ".synapse" / "workflows",
        )

        store.save(
            Workflow(
                name="my-wf",
                steps=[WorkflowStep(target="claude", message="hello")],
                scope="project",
            )
        )

        args = argparse.Namespace()

        with patch("synapse.commands.workflow.Path.cwd", return_value=project_dir):
            cmd_workflow_sync(args)

